/** Get AIPP tier label */
export const getAIPPTier = (score: number): string => {
  if (score >= 90) return "Élite";
  if (score >= 75) return "Autorité";
  if (score >= 60) return "Or";
  if (score >= 40) return "Argent";
  return "Bronze";
};

/** Get AIPP tier color class */
export const getAIPPTierColor = (score: number): string => {
  if (score >= 90) return "text-primary";
  if (score >= 75) return "text-accent";
  if (score >= 60) return "text-secondary";
  if (score >= 40) return "text-muted-foreground";
  return "text-destructive";
};
